const std = @import("std");
const Compiler = @import("zsx").Compiler;

const alloc = std.heap.page_allocator;

fn compile(src: []const u8) !struct { code: []u8, has_errors: bool } {
    var compiler = Compiler.init(alloc, src, "test.zsx", .zig);
    const result = try compiler.compile();
    return .{ .code = result.code, .has_errors = result.has_errors };
}

fn expectContains(haystack: []const u8, needle: []const u8) !void {
    if (std.mem.indexOf(u8, haystack, needle) == null) {
        std.debug.print("Expected to find:\n  \"{s}\"\nin output\n", .{needle});
        return error.TestExpectedEqual;
    }
}

fn expectNotContains(haystack: []const u8, needle: []const u8) !void {
    if (std.mem.indexOf(u8, haystack, needle) != null) {
        std.debug.print("Expected NOT to find:\n  \"{s}\"\nin output\n", .{needle});
        return error.TestExpectedEqual;
    }
}


