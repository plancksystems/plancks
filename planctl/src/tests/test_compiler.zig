test "compiler: full  with JSX" {
    const src =
        \\pub fn render(self: @This()) !Node {
        \\    return (
        \\        <div class="page">
        \\            <h1>{self.title}</h1>
        \\        </div>
        \\    );
        \\}
        \\
    ;
    const result = try compile(src);
